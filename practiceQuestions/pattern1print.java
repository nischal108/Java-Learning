public class pattern1print {
    public static void main(String[] args) {

        //wap a program to yield the following:
        //             ******
        //             ******
        //             ******

        for(int i=1;i<=3;i++)
        {
            for(int j=1;j<=6;j++)
            {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
